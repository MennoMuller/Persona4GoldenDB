--show all personas with affinities
SELECT * FROM persona
JOIN arcana ON arcana.arcananame = persona.arcananame
WHERE (phys_affinity IS NOT NULL)
    OR (fire_affinity IS NOT NULL)
    OR (ice_affinity IS NOT NULL)
    OR (elec_affinity IS NOT NULL)
    OR (wind_affinity IS NOT NULL)
    OR (dark_affinity IS NOT NULL)
    OR (light_affinity IS NOT NULL)
    OR (almighty_affinity IS NOT NULL)
ORDER BY arcana.arcananumber, arcana.arcananame, persona.baselevel ASC;